#Encode & Decode it back
vocab = {idx: bytes([idx]) for idx in range(256)}

for (p0, p1), idx in merges.items():
  vocab[idx] = vocab[p0] + vocab[p1]

#decode the list of tokens given
def decode(ids):
  '''given a list of ids return a string'''
  curr_tokens = b"".join(vocab[id] for id in ids)

  return curr_tokens.decode("utf-8", errors="replace")

print(decode([275]))

def encode(text):

  tokens = list(text.encode("utf-8"))

  while len(tokens) >= 2:

    stats = get_stats(tokens)
    min_pair = min(stats, key = lambda p : merges.get(p, float('inf')))

    #If there is no pair then it will return the first element
    if min_pair not in merges:
      break

    idx = merges[min_pair]

    tokens = merge(tokens, list(min_pair), idx)

    return tokens

#Inference check
