#Step - 5 => Loop through this replacement logic until you get the desired output

#Precompute the merges with the help of current tokens
vocab_size = 276

ids = list(tokens)

merges = {}

for new_idx in range(256, vocab_size):
  stats = get_stats(ids)
  top_pair = max(stats, key = stats.get)
  ids = merge(ids, list(top_pair), new_idx)
  merges[top_pair] = new_idx

