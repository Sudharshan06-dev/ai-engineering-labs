"""#Byte Pair Encoding Algorithm
Step - 1 => Loop through all the pairs in the encoded_str list

Step - 2 => Add the pairs as a tuple to a dictionary along with its frequency

Step - 3 => Sort the dictionary in respective to the frequency of the pairs

Step - 4 => Replace the highest frequency of pairs with the next available idx

Step - 5 => Loop through this replacement logic until you get the desired output
"""

#Step - 1 => Loop through all the pairs in the encoded_str list
#Step - 2 => Add the pairs as a tuple to a dictionary along with its frequency
def get_stats(ids):
  counts = {}
  for i in range(len(ids) - 1):
    pair = (ids[i], ids[i + 1])
    counts[pair] = counts.get(pair, 0) + 1

  return counts

stats = get_stats(tokens)

#Step - 3 => Sort the dictionary in respective to the frequency of the pairs
sorted_count = sorted(((v, k) for k, v in stats.items()), reverse = True)

top_pair = max(stats, key = stats.get)

#Step - 4 => Replace the highest frequency of pairs with the next available idx
def merge(ids, pair, idx):

  new_ids = []

  skip = False

  for i in range(len(ids)):

    if skip:
        skip = False
        continue

    if i < len(ids) - 1 and ids[i] == pair[0] and ids[i + 1] == pair[1]:
        new_ids.append(idx)
        skip = True  # This successfully skips the next element

    else:
        new_ids.append(ids[i])

  return new_ids

new_tokens = merge(tokens, list(top_pair), 256)

